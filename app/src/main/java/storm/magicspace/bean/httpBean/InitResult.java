public class InitResult {
    public String apidomain;
    public String apipath;
    public String vrdomain;
    public String vrpath;
    public String htmldomain;
    public String htmlpath;

    public String getApidomain() {
        return apidomain;
    }

    public void setApidomain(String apidomain) {
        this.apidomain = apidomain;
    }

    public String getApipath() {
        return apipath;
    }

    public void setApipath(String apipath) {
        this.apipath = apipath;
    }

    public String getVrdomain() {
        return vrdomain;
    }

    public void setVrdomain(String vrdomain) {
        this.vrdomain = vrdomain;
    }

    public String getVrpath() {
        return vrpath;
    }

    public void setVrpath(String vrpath) {
        this.vrpath = vrpath;
    }

    public String getHtmldomain() {
        return htmldomain;
    }

    public void setHtmldomain(String htmldomain) {
        this.htmldomain = htmldomain;
    }

    public String getHtmlpath() {
        return htmlpath;
    }

    public void setHtmlpath(String htmlpath) {
        this.htmlpath = htmlpath;
    }
}
